#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	sl;
	int	i;

	sl = ft_strlen(s);
	i = sl;
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)s + i);
		i--;
	}
	return (NULL);
}
